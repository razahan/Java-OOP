//  Constructor method overloading 

public class MyClass {
    
//    Constructor method *Same name of the class & it's overloaded by changing the data type
    
    MyClass(int a){
        
        System.out.println(a);
    }
    
    MyClass(double x){
        
        System.out.println(x);
    }
    
}
